Delivery_Time <- read.csv("C:/Users/Yogesh/Downloads/delivery_time (1).csv")
View(Delivery_Time)
install.packages("lattice")
library(lattice)
hist(Delivery_Time$Delivery.Time)
hist(Delivery_Time$Sorting.Time)
qqnorm(Delivery_Time$Delivery.Time)
qqline(Delivery_Time$Delivery.Time)
qqnorm(Delivery_Time$Sorting.Time)
qqline(Delivery_Time$Sorting.Time)
plot(Delivery_Time$Delivery.Time,Delivery_Time$Sorting.Time)
attach(Delivery_Time)
cor(Delivery.Time,Sorting.Time)
plot(log(Delivery.Time),Sorting.Time)
cor(Delivery.Time,Sorting.Time)
reg<-lm(Delivery.Time~Sorting.Time,data=Delivery_Time)
summary(reg)
confint(reg,level=0.95)
pred<-predict(reg,interval="predict")
predict(reg,level="predict")
pred<-as.data.frame(pred)
pred
View(pred)
cor(pred$fit,Delivery_Time$Sorting.Time)
reg_sqrt<-lm(Sorting.Time~sqrt(Delivery.Time),data=Delivery_Time)
summary(reg_sqrt)
